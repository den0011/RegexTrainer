#include "regexexamples.h"

QVector<RegexExample> RegexExamples::examples()
{
    return {
        { "Email",
          "Поиск email-адресов",
          R"(\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}\b)",
          "test@mail.com\nadmin@site.org\nwrong@email"
        },

        { "Телефон",
          "Номера телефонов +7 или 8",
          R"((\+7|8)\s?\(?\d{3}\)?\s?\d{3}-?\d{2}-?\d{2})",
          "+7 (999) 123-45-67\n8 800 555 35 35"
        },

        { "Дата",
          "Формат даты ДД.ММ.ГГГГ",
          R"(\b\d{2}\.\d{2}\.\d{4}\b)",
          "01.01.2024\n31.12.2025\n99.99.9999"
        },

        { "IP адрес",
          "IPv4 адрес",
          R"(\b(\d{1,3}\.){3}\d{1,3}\b)",
          "192.168.0.1\n10.0.0.255\n999.10.10.10"
        },

        { "HTML тег",
          "Открывающий HTML-тег",
          R"(<[a-zA-Z][a-zA-Z0-9]*>)",
          "<div>\n<span>\n<123>"
        },

        { "Числа",
          "Целые и дробные числа",
          R"(\b\d+(\.\d+)?\b)",
          "123\n45.67\ntext\n0.5\n789"
        },

        { "Заглавные слова",
          "Слова с заглавной буквы",
          R"(\b[A-ZА-Я][a-zа-я]+\b)",
          "Москва Paris london Berlin"
        },

        { "Ссылки HTTP/HTTPS",
          "Поиск URL",
          R"(https?://[^\s]+)",
          "http://site.com\nhttps://secure.org\nftp://wrong.com"
        },

        { "Простые пароли",
          "Пароли 6-12 символов, буквы и цифры",
          R"(\b[A-Za-z0-9]{6,12}\b)",
          "pass123\n12345678\nshort\nwaytoolongpassword"
        },

        { "HEX цвета",
          "Цвета в формате #RRGGBB",
          R"(#[0-9A-Fa-f]{6}\b)",
          "#FF00FF\n#123abc\n#GGHHII"
        },

        { "Домен",
          "Домен верхнего уровня",
          R"(\b[A-Za-z0-9.-]+\.[A-Za-z]{2,6}\b)",
          "google.com\nmy-site.org\nwrong_domain"
        },

        { "Только буквы",
          "Слова, содержащие только буквы",
          R"(\b[a-zA-Z]+\b)",
          "Hello world 123 test"
        },

        { "Только цифры",
          "Слова, содержащие только цифры",
          R"(\b\d+\b)",
          "123 abc 4567 89test"
        },

        { "Смайлики",
          "Простые смайлики :) или :-)",
          R"(:-?\))",
          "Hi :) Bye :-) Wrong :-("
        },

        { "Кодовые слова",
          "Слова начинаются с 'code_'",
          R"(\bcode_\w+\b)",
          "code_test code123 not_code code_abc"
        },

        { "Комментарий C++",
          "Однострочные комментарии",
          R"(//.*)",
          "// Это комментарий\nint x = 5; // комментарий"
        },

        { "Пары скобок",
          "Текст внутри круглых скобок",
          R"(\(([^)]+)\))",
          "(Hello) (World) Not(Closed"
        },

        { "Дроби",
          "Десятичные дроби",
          R"(\b\d+,\d+\b)",
          "1,23 45,67 89,0 100"
        },

        { "Время",
          "Часы и минуты HH:MM",
          R"(\b([01]?\d|2[0-3]):[0-5]\d\b)",
          "00:00 12:34 23:59 24:00"
        },

        { "HTML закрывающий тег",
          "Закрывающий HTML-тег",
          R"(</[a-zA-Z][a-zA-Z0-9]*>)",
          "</div> </span> <123>"
        }
    };
}
